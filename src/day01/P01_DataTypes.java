package day01;

public class P01_DataTypes {
    public static void main(String[] args) {
        /*
        --Primitive Data Types (Value type):
            byte, short, int, long, float, double, char, boolean

        --Non-Primitive Data Types (Reference Type):
            String, Object, Arrays, Wrapper Class
     */

            //data type   variable name   assignment operator    value   end of code
            int           age              =                      20      ;

            System.out.println("Age is " + age);

            //store name of student and print it on console
            String name  = "John";
            System.out.println("Name of student is : "+ name);

            //Store letter mark of student and print it
            char mark = 'B';
            System.out.println("Grade of student is "+ mark);
    }}
